        public HelpWindow()
        {
            InitializeComponent();

            // Версия из сборки
            var version = Assembly.GetExecutingAssembly().GetName().Version?.ToString(3) ?? "1.0.0";
            VersionText.Text = $"Версия {version}";

            // Текст справки
            HelpText.Text = @"
📌 О программе
«Финансовый Ассистент» — приложение для учёта личных финансов. Оно помогает отслеживать доходы, расходы, строить графики и формировать отчёты.

✅ Основные возможности:
• Добавление операций вручную
• Импорт выписок из банков (PDF, CSV)
• Автоматическое распознавание категорий по описанию
• Ручное назначение и редактирование категорий
• Умный учёт переводов между своими счетами (не учитываются в статистике)
• Визуализация: круговые диаграммы, гистограммы, помесячные графики
• Экспорт данных в Excel
• Управление категориями

🛠 Как использовать:

1. Импорт выписки:
   – Нажмите «Импорт выписки»
   – Выберите PDF или CSV файл
   – Для нераспознанных операций вручную укажите категорию
   – При повторном импорте с тем же описанием категория применится автоматически

2. Переводы между счетами:
   – Если в описании есть слова «перевод», «перечисление» и операции 
     совпадают по сумме и времени (±10 мин), они помечаются как внутренние
   – Такие операции НЕ учитываются в доходах/расходах

3. Категории:
   – Управляйте категориями в разделе «Настройки категорий»
   – Нельзя удалить категорию, если есть связанные операции

4. Отчёты и графики:
   – Откройте «Графики и отчёты» для анализа
   – Доступны диаграммы, таблицы, помесячная аналитика

